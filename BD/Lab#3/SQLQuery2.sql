USE X_MyBase;
GO

IF OBJECT_ID('Списания') IS NOT NULL DROP TABLE Списания;
IF OBJECT_ID('Оборудование') IS NOT NULL DROP TABLE Оборудование;
IF OBJECT_ID('Сотрудники') IS NOT NULL DROP TABLE Сотрудники;
IF OBJECT_ID('Подразделения') IS NOT NULL DROP TABLE Подразделения;
GO

CREATE TABLE Подразделения(
	[ID Подразделения] tinyint PRIMARY KEY,
	[Название подразделения] nvarchar(40)
);

CREATE TABLE Сотрудники(
	[ID Сотрудника] tinyint PRIMARY KEY,
	Фамилия nvarchar(20),
	Имя nvarchar(20),
	Отчество nvarchar(20),
	Должность nvarchar(50),
	[Дата приёма на работу] date
);

CREATE TABLE Оборудование(
	[Оборудования ID] tinyint PRIMARY KEY,
	[Название оборудования] nvarchar(30),
	[Дата поступления] datetime,
	Количество tinyint,
	[Тип оборудования] nvarchar(30) DEFAULT N'Машина' CHECK ([Тип оборудования] IN (N'gam',N'Машина',N'Шарманка'))
);


CREATE TABLE Списания(
	[ID Списания] tinyint PRIMARY KEY,
	[ID Оборудования] tinyint 
		FOREIGN KEY REFERENCES Оборудование([Оборудования ID]),
	[ID Подразделения] tinyint 
		FOREIGN KEY REFERENCES Подразделения([ID Подразделения]),
	[Причина списания] nvarchar(40),
	[Дата списания] datetime,
	[ID сотрудника] tinyint 
		FOREIGN KEY REFERENCES Сотрудники([ID Сотрудника])
);
GO

